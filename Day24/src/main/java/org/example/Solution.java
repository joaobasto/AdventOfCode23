package org.example;

public class Solution {

    double posX;
    double posY;
    double posZ;

    double velX;
    double velY;
    double velZ;

    double t1;
    double t2;
    double t3;

    public Solution(double posX, double posY, double posZ, double velX, double velY, double velZ, double t1, double t2, double t3) {
        this.posX = posX;
        this.posY = posY;
        this.posZ = posZ;
        this.velX = velX;
        this.velY = velY;
        this.velZ = velZ;
        this.t1 = t1;
        this.t2 = t2;
        this.t3 = t3;
    }
}
