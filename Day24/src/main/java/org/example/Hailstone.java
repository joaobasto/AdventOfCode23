package org.example;

public class Hailstone {

    double posX;
    double posY;
    double posZ;

    double velX;
    double velY;
    double velZ;

    public Hailstone(double posX, double posY, double posZ, double velX, double velY, double velZ) {
        this.posX = posX;
        this.posY = posY;
        this.posZ = posZ;
        this.velX = velX;
        this.velY = velY;
        this.velZ = velZ;
    }
}
